                <p>Ce document est à jour des nouvelles règles issues du Règlement n° 2016/679 du Parlement européen et du Conseil (RGPD) relatif à la protection des données à caractère personnel.

                    Une politique de confidentialité est un document qui définit de quelle manière les données des personnes naviguant sur le site sont collectées et traitées. Les personnes naviguant sur le site sont nommées "utilisateurs".

                    Ce document complète les Conditions Générales d'Utilisation d'un site internet.



                    Comment utiliser ce document ?

                    Afin d'informer l'utilisateur quel est le sort réservé à ses données, cette politique de confidentialité doit contenir plusieurs informations.

                    Tout d'abord, elle doit exposer de quelle manière sont collectées et traitées leurs données à caractère personnel, à quelles finalités le traitement et la collecte des données répond (c'est-à-dire, pourquoi les données sont collectées et traitées). Elle doit, pour chaque traitement, préciser la base légale sur laquelle s'appuie ce dernier et la durée de conservation prévue pour les données collectées.

                    Doivent être considérées comme données personnelles toutes les données étant susceptibles d'identifier un utilisateur. Il s'agit notamment du prénom et du nom, de l'âge, de l'adresse postale, l'adresse mail, la localisation de l'utilisateur ou encore son adresse IP.

                    Cette politique de confidentialité doit énoncer quels sont les droits des utilisateurs concernant ces données (par exemple, le droit d'accès aux données, droit à la suppression de ses données...), et quelles sont les procédures à suivre afin de mettre en œuvre ces droits. L'utilisateur doit notamment être informé de la possibilité qui lui est offerte d'effectuer une réclamation auprès de la CNIL.

                    La politique de confidentialité doit également informer l'utilisateur de qui est responsable du traitement des données à caractère personnel collectées et traitées. Il s'agit de la personne qui est chargée de déterminer les finalités et les moyens mis au service du traitement de ces données. Les destinataires ou catégories de destinataires des données personnelles collectées doivent être précisés.

                    Dans le cas où un Délégué à la Protection des Données a été nommé, il est nécessaire d'indiquer son identité directement dans ce document. Le rôle du Délégué à la Protection des Données est de s'assurer de la bonne mise en oeuvre des dispositions légales concernant la collecte et le traitement des données à caractère personnel (il est parfois appelé "DPO", pour Data Protection Officer).

                    Enfin, dans le cas où le site utilise des fichiers "cookies", la politique de confidentialité doit préciser quels sont les fichiers "cookies" utilisés. Un "cookie" est un fichier de petite taille (moins de 4 ko), stocké par le site sur le disque dur de l'utilisateur, contenant des informations relatives aux habitudes de navigation de l'utilisateur. Certains cookies ne peuvent être déposés sur le terminal de l'utilisateur qu'après avoir recueilli son consentement. Lorsque de tels cookies sont utilisés par le site internet, il convient d'informer l'utilisateur sur la manière dont il peut, à tout moment, retirer son consentement au dépôt de cookies.

                    La politique de confidentialité doit être facilement accessible aux utilisateurs sur le site : elle peut apparaître lors de la première visite de l'utilisateur, ou bien directement en bas de page sur le site internet par exemple.



